    /**
     * Set up sort order
     *
     * @return void
     */
    protected function setupSortOrder()
    {
        $defaultSort = ""; // Set up default sort
        if ($this->getSessionOrderBy() == "" && $defaultSort != "") {
            $this->setSessionOrderBy($defaultSort);
        }
        if (SameText(Get("cmd"), "resetsort")) {
            $this->StartRecord = 1;
            $this->CurrentOrder = "";
            $this->CurrentOrderType = "";
            $this->Help_ID->setSort("");
            $this->_Language->setSort("");
            $this->Topic->setSort("");
            $this->Description->setSort("");
            $this->Category->setSort("");
            $this->Order->setSort("");
            $this->Display_in_Page->setSort("");
            $this->Updated_By->setSort("");
            $this->Last_Updated->setSort("");

            // Save sort to session
            $this->setSessionOrderBy("");
        } else {
            $this->StartRecord = 1;
            $pageNo = Get(Config("TABLE_PAGE_NUMBER"));
            $startRec = Get(Config("TABLE_START_REC"));
            if ($pageNo !== null && is_numeric($pageNo)) { // Check for page parameter
                $this->StartRecord = ($pageNo - 1) * $this->DisplayRecords + 1;
            } elseif ($startRec !== null && is_numeric($startRec)) { // Check for "start" parameter
                $this->StartRecord = $startRec;
            }
            $this->CurrentOrder = Get("sort", "");
            $this->CurrentOrderType = Get("sortorder", "");
        }

        // Check for sort field
        if ($this->CurrentOrder !== "") {
            $this->updateSort($this->Help_ID); // Help_ID
            $this->updateSort($this->_Language); // Language
            $this->updateSort($this->Topic); // Topic
            $this->updateSort($this->Category); // Category
            $this->updateSort($this->Order); // Order
            $this->updateSort($this->Display_in_Page); // Display_in_Page
            $this->updateSort($this->Updated_By); // Updated_By
            $this->updateSort($this->Last_Updated); // Last_Updated
        }

        // Update field sort
        $this->updateFieldSort();
    }

