    // Set up foreign keys from filter
    protected function setupForeignKeysFromFilter($f)
    {
        $masterTblVar = Get("t", "");
        if ($masterTblVar == "help_categories") {
            $pattern = preg_quote("`Category`") . "\s*=\s*";
            if (preg_match("/^(" . $pattern . ")/", $f, $m)) {
                $val = substr($f, strlen($m[1]));
                $val = $this->unquoteValue($val, "DB");
                $this->Category->setQueryStringValue($val);
            }
        }
    }

    // Unquote value
    protected function unquoteValue($val, $dbid)
    {
        $dbType = GetConnectionType($dbid);
        if (StartsString("'", $val) && EndsString("'", $val)) {
            if ($dbType == "MYSQL") {
                return stripslashes(substr($val, 1, strlen($val) - 2));
            } else {
                return str_replace("''", "'", substr($val, 1, strlen($val) - 2));
            }
        } elseif ($dbType == "MSSQL" && StartsString("N'", $val) && EndsString("'", $val)) {
            return str_replace("''", "'", substr($val, 2, strlen($val) - 3));
        }
        return $val;
    }

